    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
           if (txtname.getText().isEmpty() || txtqty.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Missing Information");
        } 
           else if(AvailQty<=Integer.valueOf(txtqty.getText())){
               JOptionPane.showMessageDialog(null, "Not Enough in the Stock");
           }
           else {
            productotal= Uprice * Integer.parseInt(txtqty.getText());
            i++;
            grnTotal=grnTotal+productotal;
            if(i==1){
                TextArea1.setText("========================FAMILY POINT========================\n"+"\t NUM       PRODUCT     PRICE      QUANTITY      TOTAL\n\t"+i+"                  "+txtname.getText()+"         "+Uprice+"           "+txtqty.getText()+"              "+productotal+"\n");
            }else{
                TextArea1.setText(TextArea1.getText()+"\t"+i+"                  "+txtname.getText()+"         "+Uprice+"           "+txtqty.getText()+"              "+productotal+"\n");
            }
               
           labeltotal.setText("RS"+grnTotal);
           Update();
        }
        
        
    }//GEN-LAST:event_jButton1MouseClicked

