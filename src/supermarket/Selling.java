    private void Update(){
          newQty=AvailQty-Integer.parseInt(txtqty.getText());
         try {
                cn = con.conectar();
                ps = cn.prepareStatement("update producto set qty=? where name=?");
                ps.setInt(1, newQty);
                ps.setString(2,txtname.getText());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "producto Updated Successfully");
                cn.close();
                CleanTable();
                selectProduct();
            } catch (SQLException ex) {
                System.out.println(ex.toString());
            }
    }
    
    
    
